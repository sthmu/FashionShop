    private void deleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTNActionPerformed
        searchBTNActionPerformed(evt);
        if(!phoneNumberLBL.getText().equals("...")){
            String orderId=orderTEXT.getText();
            int i=0;
            while(!OrderController.getOrderByIndex(i).getOrderId().equals(orderId)){
                i++;
                System.out.println("value of i when iterating over to find the correct reference to delete :"+i);
            }
            OrderController.deleteOrderByIndex(i);
            JOptionPane.showMessageDialog(null,"Order Deleted");
        }
        else{
            JOptionPane.showMessageDialog(null,"Please insert a Correct Order Id");
        }
    }//GEN-LAST:event_deleteBTNActionPerformed


    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        System.out.println("Search Order Initiated");
        String orderId=orderTEXT.getText();

        Order reference=OrderController.getReferenceByOrder(orderId);
        if(reference!=null){
            phoneNumberLBL.setText(reference.getPhoneNumber());
            sizeLBL.setText(reference.getSize());
            qtyLBL.setText(String.valueOf(reference.getQty()));
            amountLBL.setText(String.valueOf(reference.getQty()*OrderController.prices[OrderController.getIndexInSizes(reference.getSize())]));
        }else{
            phoneNumberLBL.setText("...");
            sizeLBL.setText("...");
            qtyLBL.setText("...");
            amountLBL.setText("...");
        }

    }//GEN-LAST:event_searchBTNActionPerformed

